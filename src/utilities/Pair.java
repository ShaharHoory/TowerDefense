package utilities;

public class Pair {
	private int xInc;
	private int yInc;

	public Pair(int xInc, int yInc) {
		super();
		this.xInc = xInc;
		this.yInc = yInc;
	}

	public int getxInc() {
		return xInc;
	}

	public void setxInc(int xInc) {
		this.xInc = xInc;
	}

	public int getyInc() {
		return yInc;
	}

	public void setyInc(int yInc) {
		this.yInc = yInc;
	}

}
